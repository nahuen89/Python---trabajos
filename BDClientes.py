def visualizarlista(BD):
    for unArchivo in BD:
        print("--------------------------------------") 
        print("Codigo: ",unArchivo[0]) 
        print("Marca: ",unArchivo[1])
        print("Modelo: ",unArchivo[2]) 
        print("Anio: ",unArchivo[3])
        print("Cantidad: ",unArchivo[4])
        print("Precio: ",unArchivo[5])
        print("--------------------------------------")